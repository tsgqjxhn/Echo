// Helper function to get beast description
function getBeastDescription(name) {
  const lore = BeastLore[name];
  if (lore) {
    return lore.desc;
  }
  return '神秘的妖兽，实力未知，需小心应对。';
}

// Helper to get full lore with origin and weakness
function getBeastFullLore(name) {
  const lore = BeastLore[name];
  if (!lore) return { desc: '神秘的妖兽。', origin: '未知', weakness: '未知' };
  return lore;
}
